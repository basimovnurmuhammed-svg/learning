def main():
    try:
        # Запрашиваем имя
        name = input("Введите имя: ")
        print(f"Привет, {name}!")

        # Запрашиваем состояние
        mood = input("Как дела? (хорошо/плохо): ").lower().strip()

        if "хорош" in mood:
            print("Рад за тебя!")
        elif "плох" in mood:
            print("Держись, всё будет ок!")
        else:
            print("Ясно, спасибо за ответ.")
            
    except EOFError:
        # Эта часть сработает, если онлайн-редактор не поддерживает ввод
        print("\n[Ошибка]: Твой редактор не поддерживает функцию input().")
        print("Попробуй запустить этот код на компьютере через PyCharm или VS Code.")

if __name__ == "__main__":
    main()
